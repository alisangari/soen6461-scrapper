package domain;

public class AppInfo {
	private String name;
	private String score;

	public AppInfo() {

	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getScore() {
		return score;
	}

	public void setScore(String rate) {
		this.score = rate;
	}

	
}
